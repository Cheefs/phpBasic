 * 5. Написать функцию, которая заменяет в строке пробелы на подчеркивания и возвращает видоизмененную строчку.
 * @var string $string строка которую преобразуем
 * @var string $replacement то на что идет замена, по умолчанию  '_' (по условию задачи), но можно передать свой разделитель
 *
 * @return string строка где заменили пробелы на $replacement
 */
function replaceSpaces($stirng, $replacement = '_' ) {
    return str_replace(' ', $replacement, $stirng);
}
