 * 4. Объявить массив, индексами которого являются буквы русского языка,
 * а значениями – соответствующие латинские буквосочетания (‘а’=> ’a’, ‘б’ => ‘b’, ‘в’ => ‘v’, ‘г’ => ‘g’, …, ‘э’ => ‘e’, ‘ю’ => ‘yu’, ‘я’ => ‘ya’).
 * Написать функцию транслитерации строк.
*/

$translatesWords = [
    'а'=> 'a', 'б' => 'b', 'в' => 'v', 'г' => 'g',
    'д' => 'd', 'е' => 'e', 'ё' => 'yo', 'ж' => 'zh',
    'з' => 'z', 'и' => 'i', 'й' => 'j', 'к' => 'k',
    'л' => 'l', 'м' => 'm', 'н' => 'n', 'о' => 'o', 'п' => 'p',
    'р' => 'r', 'с' => 's', 'т' => 't', 'у' => 'u', 'ф' => 'f', 'х' => 'kh',
    'ц' => 'ts', 'ч' => 'ch', 'ш' => 'sh', 'щ' => 'shch', 'ъ' => '“', 'ы' => 'y',
    'э' => 'e', 'ю' => 'yu', 'я' => 'ya',
];

function transliteString($string, $array) {
    $resultStr = '';
    $words = preg_split('//u', $string);

    foreach ($words as $word) {
        if ( preg_match('/[а-яА-Я]/iu',$word) ) {
            $resultStr .= $array[mb_convert_case($word, MB_CASE_LOWER)];
        } else {
            $resultStr .= $word;
        }
    }

    return $resultStr;
}

